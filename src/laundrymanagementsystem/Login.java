    private boolean login(String role, String username, String password) {
        try {
            // Establish a database connection
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/laundry", "root", "salad123");

            // Prepare the SQL query
            String query = "SELECT * FROM user WHERE role = ? AND username = ? AND password = ?";
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, role);
            statement.setString(2, username);
            statement.setString(3, password);

            // Execute the query and check if any result is returned
            ResultSet resultSet = statement.executeQuery();
            boolean loggedIn = resultSet.next();

            // Close the database resources
            resultSet.close();
            statement.close();
            connection.close();

            return loggedIn;
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }

