    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        // Get the selected role from the combo box
        String selectedRole = RoleComboBox.getSelectedItem().toString();

        // Get the entered username and password
        String username = edtUsername.getText();
        String password = new String(edtPassword.getPassword());

        // Call the login method to validate the credentials
        boolean loggedIn = login(selectedRole, username, password);

        if (loggedIn) {
            // Navigate to the respective role page
            switch (selectedRole) {
                case "Customer":
                    // Open the customer page
                    Customer_Dashboard customerPage = new Customer_Dashboard();
                    customerPage.setVisible(true);
                    break;
                case "Admin":
                    // Open the admin page
                    AdminDashboard adminPage = new AdminDashboard();
                    adminPage.setVisible(true);
                    break;
                case "LaundryStaff":
                    // Open the laundry staff page
                    LaundryStaff_Dashboard staffPage = new LaundryStaff_Dashboard();
                    staffPage.setVisible(true);
                    break;
                case "Vendor":
                    // Open the vendor page
                    Vendor_Dashboard vendorPage = new Vendor_Dashboard();
                    vendorPage.setVisible(true);
                    break;
                default:
                    break;
            }

            // Close the login page
            this.dispose();
        } else {
            // Display an error message for invalid credentials
            JOptionPane.showMessageDialog(this, "Invalid username or password for the selected role", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

