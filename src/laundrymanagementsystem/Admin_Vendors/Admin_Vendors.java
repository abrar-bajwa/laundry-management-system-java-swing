    private void showVendors() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/laundry", "root", "salad123");
        String query = "SELECT VendorID, UserID, Name, Location, Phone, Email, Website, DeliveryAvailable, WorkingHours, Availability FROM vendor";

        Statement statement = conn.createStatement();
        ResultSet rs = statement.executeQuery(query);

        while (rs.next()) {
            String vendorID = rs.getString("VendorID");
            String userID = rs.getString("UserID");
            String name = rs.getString("Name");
            String location = rs.getString("Location");
            String phone = rs.getString("Phone");
            String email = rs.getString("Email");
            String website = rs.getString("Website");
            String deliveryAvailable = rs.getString("DeliveryAvailable");
            String workingHours = rs.getString("WorkingHours");
            String availability = rs.getString("Availability");

            model.addRow(new Object[]{vendorID, userID, name, location, phone, email, website, deliveryAvailable, workingHours, availability});
        }

        rs.close();
        statement.close();
        conn.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}


    public void scaleImage() {
        String imagePath2 = "AdminDashboardLogo.png"; // Relative path to the first image file
        String imagePath = "dashboard.png"; // Relative path to the second image file

// Load the first image file as a resource from the classpath
        URL imageUrl2 = getClass().getResource(imagePath2);

        if (imageUrl2 != null) {
            // Create an ImageIcon using the loaded resource
            ImageIcon icon2 = new ImageIcon(imageUrl2);

            // Scale the image and set it to the jLabel2 component
            Image img2 = icon2.getImage();
            Image imgScale2 = img2.getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon scaledIcon2 = new ImageIcon(imgScale2);
            jLabel2.setIcon(scaledIcon2);
        } else {
            System.out.println("Image not found: " + imagePath2);
        }

// Load the second image file as a resource from the classpath
        URL imageUrl = getClass().getResource(imagePath);

        if (imageUrl != null) {
            // Create an ImageIcon using the loaded resource
            ImageIcon icon = new ImageIcon(imageUrl);

            // Scale the image and set it to the jLabel3 component
            Image img = icon.getImage();
            Image imgScale = img.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon scaledIcon = new ImageIcon(imgScale);
            jLabel3.setIcon(scaledIcon);
        } else {
            System.out.println("Image not found: " + imagePath);
        }

    }

