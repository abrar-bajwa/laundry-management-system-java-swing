    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        // No vendor selected, show an error message or take appropriate action
        return;
    }

    String vendorID = jTable1.getValueAt(selectedRow, 0).toString();

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/laundry", "root", "salad123");
        String query = "DELETE FROM vendor WHERE VendorID = ?";

        PreparedStatement statement = conn.prepareStatement(query);
        statement.setString(1, vendorID);
        int rowsDeleted = statement.executeUpdate();

        if (rowsDeleted > 0) {
            // Vendor deleted successfully, show success message or take appropriate action
            System.out.println("Vendor deleted successfully");
            // Refresh the vendor table after deletion
            showVendors();
        } else {
            // Vendor deletion failed, show an error message or take appropriate action
            System.out.println("Failed to delete vendor");
        }

        statement.close();
        conn.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

